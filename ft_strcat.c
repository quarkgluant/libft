#include "libft.h"

char		*ft_strcat(char *s1, const char *s2)
{
	size_t	len;
	size_t	i;

	len = ft_strlen(s1);
	i = 0;
	while (s2[i])
		s1[len++] = ((char *)s2)[i++];
	s1[len] = 0;
	return (s1);
}
